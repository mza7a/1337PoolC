int		ft_atoi(char *str)
{
	int	sign;
	int	res;
	int	c;

	c = 0;
	sign = 1;
	res = 0;
	if (str[0] == '-')
	{
		sign = -1;
		c++;
	}
	else if (str[0] == '+' || str[c] == ' ' || str[c] == '\n' || str[c] == '\t')
	{
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		res = res * 10 + str[c] - '0';
		c++;
	}
	return (sign * res);
}
