char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	char	*j;

	while (*str != '\0')
	{
		i = 0;
		j = str;
		while (*str == to_find[i] && to_find[i] != '\0')
		{
			str++;
			i++;
		}
		if (to_find[i] == '\0')
			return (j);
		str = j + 1;
	}
	return (0);
}
