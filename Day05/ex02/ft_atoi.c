int		ft_atoi(char *str)
{
	int sign;
	int i;
	int res;

	sign = 1;
	i = 0;
	res = 0;
	if (str[0] == '-')
	{
		i++;
		sign = -1;
	}
	else if (str[0] == '+' || str[i] == ' ' || str[i] == '\n' || str[i] == '\t')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (sign * res);
}
